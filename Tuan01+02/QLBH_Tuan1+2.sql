--Bai tap 1, phan II, cau 3, trang 4
update SANPHAM1 set GIA = GIA + GIA*0.05
where NUOCSX = 'Thai Lan'

--Bai tap 1, phan II, cau 4, trang 4
update SANPHAM1 set GIA = GIA - GIA*0.05
where NUOCSX = 'Trung Quoc' and GIA <= 10000

--Bai tap 1, phan II, cau 5, trang 4
update KHACHHANG1 set LOAIKH = 'Vip'
where (NGDK < '1/1/2007' and DOANHSO >= 10000000) or (NGDK >= '1/1/2007' and DOANHSO >= 2000000)


--Bai tap 1, phan III, cau 1, trang 4
select MASP, TENSP from SANPHAM
where NUOCSX = 'Trung Quoc'

--Bai tap 1, phan III, cau 2, trang 4
select MASP, TENSP from SANPHAM
where DVT = 'cay' or DVT = 'quyen'

--Bai tap 1, phan III, cau 3, trang 4
select MASP, TENSP from SANPHAM
where MASP like 'B_01'

--Bai tap 1, phan III, cau 4, trang 4
select MASP, TENSP from SANPHAM
where NUOCSX = 'Trung Quoc' and (GIA between 30000 and 40000)

--Bai tap 1, phan III, cau 5, trang 4
select MASP, TENSP from SANPHAM
where (NUOCSX = 'Trung Quoc' or NUOCSX = 'Thai Lan') and (GIA between 30000 and 40000)

--Bai tap 1, phan III, cau 6, trang 4
select SOHD, TRIGIA from HOADON 
where NGHD IN ('1/1/2007', '2/1/2007')

--Bai tap 1, phan III, cau 7, trang 4
select SOHD, TRIGIA from HOADON
where YEAR(NGHD) = 2007 and MONTH(NGHD) = 1
order by NGHD asc, TRIGIA desc

--Bai tap 1, phan III, cau 8, trang 4
select HD.MAKH, HOTEN from HOADON HD inner join KHACHHANG KH
on HD.MAKH = KH.MAKH
where NGHD = '1/1/2007'

--Bai tap 1, phan III, cau 9, trang 4
select HD.SOHD, TRIGIA from HOADON HD inner join NHANVIEN NV
on HD.MANV = NV.MANV
where HOTEN = 'Nguyen Van B' and NGHD = '28/10/2006'

--Bai tap 1, phan III, cau 10, trang 4
select SP.MASP, TENSP from CTHD CT inner join SANPHAM SP
on CT.MASP = SP.MASP
where SOHD in (
	select SOHD
	from HOADON HD inner join KHACHHANG KH
	on HD.MAKH = KH.MAKH
	where HOTEN = 'Nguyen Van A' and YEAR(NGHD) = 2006 and MONTH(NGHD) = 10
)

--Bai tap 1, phan III, cau 11, trang 4
select SOHD from CTHD 
where MASP = 'BB01' or MASP = 'BB02'
