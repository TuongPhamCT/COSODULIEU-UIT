--Tao CSDL QUANLYBANHANG
create database QLBH
--Bai tap 1, phan I, cau 1, trang 3
create table KHACHHANG
(
	MAKH char(4) not null,
	HOTEN varchar(40),
	DCHI varchar(50),
	SODT varchar(20),
	NGSINH smalldatetime,
	NGDK smalldatetime,
	DOANHSO money
)

create table NHANVIEN
(
	MANV char(4) not null,
	HOTEN varchar(40),
	SODT varchar(20),
	NGVL smalldatetime
)

create table SANPHAM
(
	MASP char(4) not null,
	TENSP varchar(40),
	DVT varchar(20),
	NUOCSX varchar(40),
	GIA money
)

create table HOADON
(
	SOHD int not null,
	NGHD smalldatetime,
	MAKH char(4),
	MANV char(4),
	TRIGIA money
)

create table CTHD
(
	SOHD int not null,
	MASP char(4) not null,
	SL int
)

alter table KHACHHANG add constraint PK_KH primary key (MAKH)
alter table NHANVIEN add constraint PK_NV primary key (MANV)
alter table SANPHAM add constraint PK_SP primary key (MASP)
alter table HOADON add constraint PK_HD primary key (SOHD)
alter table CTHD add constraint PK_CTHD primary key (SOHD, MASP)

alter table HOADON add constraint FK_HD_KH foreign key (MAKH) references KHACHHANG(MAKH),
constraint FK_HD_NV foreign key (MANV) references NHANVIEN(MANV)

alter table CTHD add constraint FK_CTHD_HD foreign key (SOHD) references HOADON(SOHD),
constraint FK_CTHD_SP foreign key (MASP) references SANPHAM(MASP)

